ASSUME CS:CODE, DS:DATA
DATA SEGMENT
ARRAY DW 75H, 72H, 80H, 18H, 76H, 09H
COUNT EQU 06H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
MOV DX, COUNT-1

LOOP1: MOV CX, DX
MOV SI, OFFSET ARRAY

LOOP2: MOV AX,[SI]
CMP AX,2H[SI]
JC END1
XCHG [SI+2],AX
XCHG [SI],AX

END1: ADD SI,02H
LOOP LOOP2
DEC DX
JNZ LOOP1

HLT
CODE ENDS
END START
